const catalogs = { en, he };

export function uiLang(code) {
  if (code && catalogs[code]) return code;
  return DEFAULT_LANGUAGE === 'he' ? 'he' : 'en';
}

export function translate(key, lang = DEFAULT_LANGUAGE, vars = {}) {
  const catalog = catalogs[uiLang(lang)] || catalogs.en;
  let text = catalog[key] ?? catalogs.en[key] ?? key;
  Object.entries(vars).forEach(([k, v]) => {
    text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  });
  return text;
}

export function uiStringsFor(lang) {
  const code = uiLang(lang);
  return { ...(catalogs.en || {}), ...(catalogs[code] || {}) };
}
